package Third;

public class Twentyfirst {

	public static void main(String[] args) {
		int a=400;
		if(a<=100)
			System.out.println("80/-");
		else if(a>100 && a<=200)
			System.out.println((a*1)+"/-");
		else if(a>200 && a<=400)
			System.out.println((a*2)+"/-");
		else if(a>400 && a<=500)
			System.out.println((a*3)+"/-");
		else
			System.out.println((a*4)+"/-");

	}

}
